async function main() {
  const hash = bcrypt.hashSync('123456', 10);

  // Mốc ma_user hiện tại (U####)
  const { rows: mx } = await pool.query(
    "SELECT COALESCE(MAX(NULLIF(regexp_replace(ma_user,'\\D','','g'),''))::int,0) AS m FROM nguoi_dung"
  );
  let next = mx[0].m + 1;

  const lines = RAW.split('\n').map((l) => l.trim()).filter(Boolean);
  let added = 0; let skipped = 0; const seen = new Set();

  for (const line of lines) {
    const parts = line.split(/\s+/);
    const username = parts.pop();
    const hoTen = parts.join(' ').trim();
    if (!username || !hoTen) { console.log('BỎ (không parse được):', JSON.stringify(line)); skipped += 1; continue; }
    if (seen.has(username)) { skipped += 1; continue; }
    seen.add(username);

    const { rows: ex } = await pool.query('SELECT 1 FROM nguoi_dung WHERE ten_dang_nhap = $1', [username]);
    if (ex.length) { skipped += 1; continue; }

    const maUser = 'U' + String(next).padStart(4, '0');
    next += 1;
    await pool.query(
      `INSERT INTO nguoi_dung (ma_user, ten_dang_nhap, mat_khau_hash, ho_ten, trang_thai, dang_hoat_dong)
       VALUES ($1,$2,$3,$4,'ACTIVE',true)`,
      [maUser, username, hash, hoTen]
    );
    added += 1;
  }

  console.log(`Xong: thêm ${added} người dùng, bỏ qua ${skipped} (trùng/không hợp lệ). Mật khẩu: 123456`);
  await pool.end();
}

main().catch((e) => { console.error('LỖI:', e.message); process.exit(1); });
